int32 Audio_WMAGetDurationTime(HANDLE FileHander, INT32 filesize,INT32 BeginPlayProgress,INT32 OffsetPlayProgress)
{

    int i,result,DurationLenth;
    tWMAFileStatus          iResult;                // api result
    tWMAFileStateInternal WMAFileState;
    uint8 *pchar;

    pchar = (int8 *)(&WMAFileState);
    for(i=0; i<sizeof(tWMAFileStateInternal); i++)
        *pchar++=0;

    WMAFileState.hdr_parse.pInput = &FileHander;
    // initialize decoder
    iResult = WMAFileGetInfo((tHWMAFileState)(&WMAFileState));
    if(cWMA_NoErr != iResult)   return HAL_ERR_RESOURCE_NOT_ENABLED;

    DurationLenth=(UINT32)(((UINT64)(OffsetPlayProgress)*(UINT64)filesize)/10000);

    result=DurationLenth*(UINT64)(WMAFileState.hdr_parse.msDuration)/filesize;

    diag_printf("[WMA]msDuration = %d\n",WMAFileState.hdr_parse.msDuration);

    return  result;
}



