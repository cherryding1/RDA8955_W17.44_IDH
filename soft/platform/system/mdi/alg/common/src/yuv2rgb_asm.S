	.text
	.globl VidRec_yuv2rgb
	.ent VidRec_yuv2rgb
	.set noreorder	
VidRec_yuv2rgb:	
	subu 	sp, 	sp, 	64 	
	sw 	ra, 	60(sp)
	sw 	fp, 	56(sp)
	sw 	s0, 	52(sp)
	sw 	s1, 	48(sp)
	sw 	s2, 	44(sp)
	sw 	s3, 	40(sp)
	sw 	s4, 	36(sp)
	sw 	s5, 	32(sp)
	sw 	s6, 	28(sp)
	sw 	s7, 	24(sp)	
	li	s0,	454
	li	s1,	88
	li	s2,	183
	li	s3,	359
	la	s4,	red_clip
	la	s5,	green_clip
	la	s6,	blue_clip
	li	t0,	0xdfffffff	
	move	v1,	a1
	lw	s7,	80(sp)
	and	a0,	a0,	t0
	cache 	2,	0
	nop
	nop
	nop

loop_height:
loop_width:
	lbu	t0,	0(a0)
	lbu	t1,	1(a0)
	lbu	t3,	3(a0)
	addiu	t1,	t1,	-128
	addiu	t3,	t3,	-128

	mult	t1,	s0
	lbu	t2,	2(a0)		#remove nop
	mflo	t4
	
	mult 	t1,	s1
	sra	t4,	t4,	0x8	#t4 = U*454>>8
	madd	t3,	s2
	addu	t7,	t0,	t4
	mflo	t5
	sra	t5,	t5,	0x8	#t4 = (U*88+V*183)>>8
	
	mult	t3,	s3
	subu	t8,	t0,	t5
	mflo	t6
	sra	t6,	t6,	0x8	#t6 = V*359>>8
	
	addiu	t7,	t7,	0x4	#R
	sra	t7,	t7,	0x3
	sll	t7,	t7,	0x1
	addu	t7,	t7,	s4
	lh	t7,	0(t7)
	
	addiu	t8,	t8,	0x2	#G
	sra	t8,	t8,	0x2
	sll	t8,	t8,	0x1
	addu	t8,	t8,	s5
	lh	t8,	0(t8)
	
	addu	t9,	t0,	t6
	addiu	t9,	t9,	0x4	#B
	sra	t9,	t9,	0x3
	sll	t9,	t9,	0x1
	addu	t9,	t9,	s6
	lh	t9,	0(t9)
	
	or	v0,	t7,	t8
	or	v0,	v0,	t9
	sh	v0,	0(a3)

	addu	t7,	t2,	t4
	sra	t7,	t7,	0x3
	sll	t7,	t7,	0x1
	addu	t7,	t7,	s4
	lh	t7,	0(t7)
	
	subu	t8,	t2,	t5
	sra	t8,	t8,	0x2
	sll	t8,	t8,	0x1
	addu	t8,	t8,	s5
	lh	t8,	0(t8)
	
	addu	t9,	t2,	t6
	sra	t9,	t9,	0x3
	sll	t9,	t9,	0x1
	addu	t9,	t9,	s6
	lh	t9,	0(t9)
	
	or	v0,	t7,	t8
	or	v0,	v0,	t9
	sh	v0,	2(a3)
	
	addiu	a0,	a0,	0x4
	addiu	v1,	v1,	-0x2
	bne	v1,	zero,	loop_width
	addiu	a3,	a3,	0x4
	addu	a3,	a3,	s7	
	addiu	a2,	a2,	-1	
	bne	a2,	zero,	loop_height	
	move	v1,	a1
	
	lw 	ra, 	60(sp)	
	lw 	fp, 	56(sp)	
	lw 	s0, 	52(sp)	
	lw 	s1, 	48(sp)	
	lw 	s2, 	44(sp)	
	lw 	s3, 	40(sp)	
	lw 	s4, 	36(sp)	
	lw 	s5, 	32(sp)	
	lw 	s6, 	28(sp)	
	lw 	s7, 	24(sp)	
	lw	v0,	20(sp)
	lw	v1,	16(sp)
	jr 	ra 			
	addu 	sp, 	sp, 	64 	
	.set reorder
	.end VidRec_yuv2rgb

